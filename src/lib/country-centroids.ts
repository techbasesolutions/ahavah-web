/**
 * Look up the centroid for an ISO-3166-1 alpha-2 country code.
 *
 * Returns `null` if the code is unknown or has no centroid data. All 250
 * entries in {@link ALL_COUNTRIES} are covered.
 *
 * @example
 *   centroidOf("BB"); // { lat: 13.1832, lng: -59.5574 }
 *   centroidOf("XX"); // null
 */
export function centroidOf(iso2: string): Centroid | null {
  if (typeof iso2 !== "string" || iso2.length !== 2) return null;
  const key = iso2.toUpperCase();
  return COUNTRY_CENTROIDS[key] ?? null;
}
